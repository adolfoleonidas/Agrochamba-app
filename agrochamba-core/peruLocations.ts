// =============================================================================

/**
 * Valida si una ubicación completa es válida según peruLocations
 */
export function isValidLocation(ubicacion: UbicacionCompleta): boolean {
  const dep = departamentosPeru.find(d => 
    normalizeText(d.departamento) === normalizeText(ubicacion.departamento)
  );
  if (!dep) return false;
  
  const prov = dep.provincias.find(p => 
    normalizeText(p.provincia) === normalizeText(ubicacion.provincia)
  );
  if (!prov) return false;
  
  return prov.distritos.some(d => 
    normalizeText(d) === normalizeText(ubicacion.distrito)
  );
}

/**
 * Valida y normaliza una ubicación (corrige mayúsculas/tildes)
 */
export function normalizeLocation(ubicacion: UbicacionCompleta): UbicacionCompleta | null {
  for (const dep of departamentosPeru) {
    if (normalizeText(dep.departamento) === normalizeText(ubicacion.departamento)) {
      for (const prov of dep.provincias) {
        if (normalizeText(prov.provincia) === normalizeText(ubicacion.provincia)) {
          for (const dist of prov.distritos) {
            if (normalizeText(dist) === normalizeText(ubicacion.distrito)) {
              return {
                departamento: dep.departamento, // Nombre correcto
                provincia: prov.provincia,       // Nombre correcto
                distrito: dist,                  // Nombre correcto
                direccion: ubicacion.direccion,
                coordenadas: ubicacion.coordenadas,
              };
            }
          }
        }
      }
    }
  }
  return null;
}

