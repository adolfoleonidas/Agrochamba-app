/**
 * Normaliza texto para búsqueda (quita tildes, minúsculas, espacios extras)
 */
export function normalizeText(text: string): string {
  return text
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '') // Remover tildes
    .replace(/ñ/g, 'n')
    .replace(/\s+/g, ' ')
    .trim();
}

/**
 * Calcula score de similitud entre dos strings (0-100)
 */
function calculateSimilarity(query: string, target: string): number {
  const normalizedQuery = normalizeText(query);
  const normalizedTarget = normalizeText(target);
  
  // Match exacto
  if (normalizedTarget === normalizedQuery) return 100;
  
  // Empieza con el query
  if (normalizedTarget.startsWith(normalizedQuery)) return 90;
  
  // Contiene el query
  if (normalizedTarget.includes(normalizedQuery)) return 70;
  
  // Query contiene el target (búsqueda parcial inversa)
  if (normalizedQuery.includes(normalizedTarget)) return 50;
  
  // Sin match
  return 0;
}

