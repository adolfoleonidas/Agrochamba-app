/**
 * Tipo de ubicación en la jerarquía
 */
export type LocationType = 'departamento' | 'provincia' | 'distrito';

/**
 * Ubicación completa con los 3 niveles jerárquicos
 */
export interface UbicacionCompleta {
  departamento: string;
  provincia: string;
  distrito: string;
  direccion?: string;
  coordenadas?: {
    lat: number;
    lng: number;
  };
}

/**
 * Resultado de búsqueda de ubicación
 */
export interface LocationSearchResult {
  texto: string;
  tipo: LocationType;
  departamento: string;
  provincia?: string;
  distrito?: string;
  displayLabel: string;
  // Score de relevancia (mayor = más relevante)
  score: number;
}

/**
 * Sede de empresa
 */
export interface SedeEmpresa {
  id: string;
  nombre: string;
  ubicacion: UbicacionCompleta;
  esPrincipal: boolean;
  activa: boolean;
}

