/**
 * Obtiene estadísticas del dataset de ubicaciones
 */
export function getLocationStats(): {
  totalDepartamentos: number;
  totalProvincias: number;
  totalDistritos: number;
} {
  let totalProvincias = 0;
  let totalDistritos = 0;
  
  for (const dep of departamentosPeru) {
    totalProvincias += dep.provincias.length;
    for (const prov of dep.provincias) {
      totalDistritos += prov.distritos.length;
    }
  }
  
  return {
    totalDepartamentos: departamentosPeru.length,
    totalProvincias,
    totalDistritos,
  };
}

/**
 * Obtiene lista de departamentos con conteo de distritos
 */
export function getDepartamentosWithCount(): { nombre: string; distritos: number }[] {
  return departamentosPeru.map(dep => {
    let distritos = 0;
    for (const prov of dep.provincias) {
      distritos += prov.distritos.length;
    }
    return { nombre: dep.departamento, distritos };
  });
}

