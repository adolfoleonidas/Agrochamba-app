// =============================================================================

/**
 * Resuelve la ubicación completa desde un distrito
 * Útil cuando solo tienes el nombre del distrito
 * 
 * @param distrito Nombre del distrito
 * @returns Ubicación completa o null si no se encuentra
 */
export function resolveFromDistrito(distrito: string): UbicacionCompleta | null {
  const normalizedDistrito = normalizeText(distrito);
  
  for (const dep of departamentosPeru) {
    for (const prov of dep.provincias) {
      for (const dist of prov.distritos) {
        if (normalizeText(dist) === normalizedDistrito) {
          return {
            departamento: dep.departamento,
            provincia: prov.provincia,
            distrito: dist,
          };
        }
      }
    }
  }
  return null;
}

/**
 * Resuelve la ubicación completa desde una provincia
 * Devuelve con el primer distrito de esa provincia
 */
export function resolveFromProvincia(provincia: string, departamento?: string): UbicacionCompleta | null {
  const normalizedProvincia = normalizeText(provincia);
  
  for (const dep of departamentosPeru) {
    // Si se especifica departamento, filtrar
    if (departamento && normalizeText(dep.departamento) !== normalizeText(departamento)) {
      continue;
    }
    
    for (const prov of dep.provincias) {
      if (normalizeText(prov.provincia) === normalizedProvincia) {
        return {
          departamento: dep.departamento,
          provincia: prov.provincia,
          distrito: prov.distritos[0], // Primer distrito
        };
      }
    }
  }
  return null;
}

/**
 * Resuelve ubicación desde un LocationSearchResult
 */
export function resolveFromSearchResult(result: LocationSearchResult): UbicacionCompleta | null {
  switch (result.tipo) {
    case 'distrito':
      return {
        departamento: result.departamento,
        provincia: result.provincia!,
        distrito: result.distrito!,
      };
    case 'provincia':
      return resolveFromProvincia(result.provincia!, result.departamento);
    case 'departamento':
      // Devolver primera provincia y primer distrito
      const dep = departamentosPeru.find(d => d.departamento === result.departamento);
      if (dep && dep.provincias.length > 0) {
        const prov = dep.provincias[0];
        return {
          departamento: dep.departamento,
          provincia: prov.provincia,
          distrito: prov.distritos[0],
        };
      }
      return null;
    default:
      return null;
  }
}

