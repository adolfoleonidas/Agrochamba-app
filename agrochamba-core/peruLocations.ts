// Función para obtener las provincias de un departamento específico
export function getProvincias(departamento: string): string[] {
  const dep = departamentosPeru.find(d => d.departamento === departamento);
  if (!dep) return [];
  return dep.provincias.map(prov => prov.provincia);
}

// Función para obtener los distritos de una provincia específica
export function getDistritos(departamento: string, provincia: string): string[] {
  const dep = departamentosPeru.find(d => d.departamento === departamento);
  if (!dep) return [];
  
  const prov = dep.provincias.find(p => p.provincia === provincia);
  if (!prov) return [];
  
  return prov.distritos;
}

// Función para obtener la ubicación completa formateada
export function getFullLocation(departamento: string, provincia?: string, distrito?: string): string {
  if (!departamento) return "";
  
  let location = departamento;
  
  if (provincia) {
    location += ", " + provincia;
    
    if (distrito) {
      location += ", " + distrito;
    }
  }
  
  return location;
}

