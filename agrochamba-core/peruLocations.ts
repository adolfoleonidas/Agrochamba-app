/**
 * Búsqueda inteligente de ubicaciones
 * Busca en departamentos, provincias y distritos simultáneamente
 * Ordena por relevancia y evita duplicados
 * 
 * @param query Texto a buscar
 * @param limit Máximo de resultados (default: 10)
 * @returns Lista de resultados ordenados por relevancia
 */
export function searchLocation(query: string, limit: number = 10): LocationSearchResult[] {
  if (!query || query.trim().length < 2) return [];
  
  const results: LocationSearchResult[] = [];
  const normalizedQuery = normalizeText(query);
  const seen = new Set<string>(); // Para evitar duplicados
  
  for (const dep of departamentosPeru) {
    const depNormalized = normalizeText(dep.departamento);
    
    // Buscar en departamentos
    if (depNormalized.includes(normalizedQuery) || normalizedQuery.includes(depNormalized)) {
      const score = calculateSimilarity(query, dep.departamento);
      const key = `dep:${dep.departamento}`;
      if (score > 0 && !seen.has(key)) {
        seen.add(key);
        results.push({
          texto: dep.departamento,
          tipo: 'departamento',
          departamento: dep.departamento,
          displayLabel: `📍 ${dep.departamento} (Departamento)`,
          score: score + 10, // Bonus para departamentos
        });
      }
    }
    
    for (const prov of dep.provincias) {
      const provNormalized = normalizeText(prov.provincia);
      
      // Buscar en provincias
      if (provNormalized.includes(normalizedQuery) || normalizedQuery.includes(provNormalized)) {
        const score = calculateSimilarity(query, prov.provincia);
        const key = `prov:${dep.departamento}:${prov.provincia}`;
        if (score > 0 && !seen.has(key)) {
          seen.add(key);
          results.push({
            texto: prov.provincia,
            tipo: 'provincia',
            departamento: dep.departamento,
            provincia: prov.provincia,
            displayLabel: `🏘️ ${prov.provincia}, ${dep.departamento}`,
            score: score + 5, // Bonus para provincias
          });
        }
      }
      
      for (const dist of prov.distritos) {
        const distNormalized = normalizeText(dist);
        
        // Buscar en distritos
        if (distNormalized.includes(normalizedQuery) || normalizedQuery.includes(distNormalized)) {
          const score = calculateSimilarity(query, dist);
          const key = `dist:${dep.departamento}:${prov.provincia}:${dist}`;
          if (score > 0 && !seen.has(key)) {
            seen.add(key);
            results.push({
              texto: dist,
              tipo: 'distrito',
              departamento: dep.departamento,
              provincia: prov.provincia,
              distrito: dist,
              displayLabel: `📌 ${dist}, ${prov.provincia}, ${dep.departamento}`,
              score: score,
            });
          }
        }
      }
    }
  }
  
  // Ordenar por score (mayor primero) y limitar resultados
  return results
    .sort((a, b) => b.score - a.score)
    .slice(0, limit);
}

/**
 * Búsqueda rápida que devuelve solo distritos (para selector simplificado)
 */
export function searchDistritos(query: string, limit: number = 10): LocationSearchResult[] {
  return searchLocation(query, limit * 2)
    .filter(r => r.tipo === 'distrito')
    .slice(0, limit);
}

