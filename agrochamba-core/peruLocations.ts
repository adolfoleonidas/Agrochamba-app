/**
 * Crea una nueva sede con ID único
 */
export function createSede(
  nombre: string,
  ubicacion: UbicacionCompleta,
  esPrincipal: boolean = false
): SedeEmpresa {
  return {
    id: `sede_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`,
    nombre,
    ubicacion,
    esPrincipal,
    activa: true,
  };
}

/**
 * Valida una sede completa
 */
export function isValidSede(sede: SedeEmpresa): boolean {
  return Boolean(
    sede.id &&
    sede.nombre &&
    sede.nombre.trim().length > 0 &&
    isValidLocation(sede.ubicacion)
  );
}

